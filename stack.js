/*
push() - add element at end , returns new length of the array
unshift() - add element at beginning , returns new length of array
pop() - remove element from end , returns removed element
shift() - remove element from beginning , returns removed element
*/

let arr = [20,30,40]
console.log(arr)

console.log(arr.push(50))

console.log(arr)

console.log(arr.unshift(10))

console.log(arr)

console.log(arr.pop())

console.log(arr)

console.log(arr.shift())

console.log(arr)